package HelloWorld;

public class Myfirstpro {
	//datatypes of java
	public static void main(String[]args) {
		int intval=45;
		short sval=5;
		boolean boolval=false;
		char cval='S';
		double dval=3000d;                           //datatypes with assigning values
		long lval=300000000000000000l;
		float fval=10000.0f;
		byte bval=0;
		 
		System.out.println("Integer value "+intval);
		System.out.println("short value "+sval);
		System.out.println("boolean value "+boolval);
		System.out.println("char value "+cval);                  //display the values
        System.out.println("double value "+dval);
        System.out.println("long value "+lval);
        System.out.println("float value "+fval);
        System.out.println("byte value "+bval);
        
        
	}
	}
