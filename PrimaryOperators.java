package HelloWorld;
/*primary operators*/
public class PrimaryOperators{
	public static void main(String[]args) {
	double num1,num2,ans;//using double datatype
	num1 =40000d;//assigning value
	num2=20000d;//assigning value
	
	ans=num1+num2;//addition
	System.out.println("The sum of "+num1+" and "+num2+" is "+ans);//display
	ans=num1-num2;//subtraction
	System.out.println("The difference of "+num1+" and "+num2+" is "+ans);//display
	ans=num1*num2;//multiply
	System.out.println("The product of "+num1+" and "+num2+" is "+ans);//display
	ans=num1/num2;//divider
	System.out.println("The quotient of "+num1+" and "+num2+" is "+ans);//display
	ans=num1%num2;//remainder
	System.out.println("The remainder of "+num1+" and "+num2+" is "+ans);//display
}
}
